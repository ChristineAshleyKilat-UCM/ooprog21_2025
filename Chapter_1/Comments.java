/**
   This is a documentation comment 
   This program contains the 3-different types of comments in java
   1. Single-line comment
   2. Multi-line comment
   3. Documentation comment
*/
public class Comments {
   public static void main(String[] args) {
   
   // This is a single-line comment
   System.out.println("A single-line comment");
   /*
    This is a multi-line comment
    Useful when your classmate will look at your code
    It can save you time to explain it to them
    Be careful though because some of them will copy your code*/
   System.out.println("A multi-line comment");
   
   }
}
    
  
    
    